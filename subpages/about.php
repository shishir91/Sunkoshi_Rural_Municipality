            Sunkoshi Rural Municipality is a rural municipality in Sindhuli district of Bagmati Province in Nepal. The Rural municipality is divided into 7 wards. According to 2011 Nepal census, the total population of the municipality is 21,473 with 4,557 households. The total area of the municipality is 158.68 square kilometres (61.27 sq mi).
            The headquarter of the municipality is at Jhangajholi Ratmata.
